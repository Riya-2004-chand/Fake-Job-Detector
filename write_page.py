content += '      })\n'
content += '      const data = await res.json()\n'
content += '      setResult(data)\n'
content += '    } catch (e) {\n'
content += '      alert("API not running")\n'
content += '    }\n'
content += '    setLoading(false)\n'
content += '  }\n\n'
content += '  return (\n'
content += '    <main className="min-h-screen bg-gray-950 text-white p-8">\n'
