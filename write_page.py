content += '            {result.flags.length > 0 && (\n'
content += '              <ul>\n'
content += '                {result.flags.map((flag, i) => (\n'
content += '                  <li key={i} className="text-red-300">🚩 {flag}</li>\n'
content += '                ))}\n'
content += '              </ul>\n'
content += '            )}\n'
content += '          </div>\n'
content += '        )}\n'
content += '      </div>\n'
content += '    </main>\n'
content += '  )\n'
content += '}\n'
with open(r'C:\fake-job-frontend\app\page.js', 'w', encoding='utf-8') as f:
    f.write(content)
