content += '        <p className="text-gray-400 mb-6">Paste a job description to check if it is fraudulent.</p>\n'
content += '        <textarea\n'
content += '          className="w-full h-48 bg-gray-800 text-white rounded-lg p-4 mb-4 border border-gray-700"\n'
content += '          placeholder="Paste job description here..."\n'
content += '          value={text}\n'
content += '          onChange={e => setText(e.target.value)}\n'
content += '        />\n'
